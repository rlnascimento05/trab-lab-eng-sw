CREATE TABLE Medicamento(
	CodMedicamento INT,
	NomeProduto CHAR(100),
	TipoProduto CHAR(100),
	Fabricante VARCHAR (100),
	Descricao VARCHAR(255),
	Valor DOUBLE,
	Peso INT,
	Validade DATE,
	QtdCapsula INT,
	Generico BOOLEAN,
	PrincipioAtivo CHAR(255),
	Tarja BOOLEAN,
	ContraIndicacao CHAR(255),
	ReacoesAdversas CHAR(255),
	Percacoes CHAR(225)
);
